package org.woped.metrics.metricsCalculation;

public class StringPair {
	
	private String key;
	private String value;
	
	public StringPair(String key, String value){
		this.key = key;
		this.value = value;
	}
	
	public String getKey() {
		return key;
	}
	public String getValue() {
		return value;
	}

}
